import Phaser from 'phaser'

export default class BootScene extends Phaser.Scene {
  constructor() {
    super({ key: 'BootScene' })
  }

  create(): void {
    const g = this.make.graphics({ x: 0, y: 0 }, false)

    // player (16x24)
    g.fillStyle(0xffd5b8, 1)
    g.fillRect(4, 0, 8, 8)
    g.fillStyle(0x4a3728, 1)
    g.fillRect(4, 0, 8, 3)
    g.fillStyle(0xe74c3c, 1)
    g.fillRect(3, 8, 10, 8)
    g.fillRect(1, 9, 2, 6)
    g.fillRect(13, 9, 2, 6)
    g.fillStyle(0x2c3e50, 1)
    g.fillRect(4, 16, 3, 6)
    g.fillRect(9, 16, 3, 6)
    g.fillStyle(0x333333, 1)
    g.fillRect(3, 22, 4, 2)
    g.fillRect(9, 22, 4, 2)
    g.generateTexture('player', 16, 24)
    g.clear()

    // player_jump (16x24)
    g.fillStyle(0xffd5b8, 1)
    g.fillRect(4, 0, 8, 8)
    g.fillStyle(0x4a3728, 1)
    g.fillRect(4, 0, 8, 3)
    g.fillStyle(0xe74c3c, 1)
    g.fillRect(3, 8, 10, 8)
    g.fillRect(1, 9, 2, 6)
    g.fillRect(13, 9, 2, 6)
    g.fillStyle(0x2c3e50, 1)
    g.fillRect(5, 16, 6, 5)
    g.fillStyle(0x333333, 1)
    g.fillRect(4, 21, 8, 3)
    g.generateTexture('player_jump', 16, 24)
    g.clear()

    // ground (16x16)
    g.fillStyle(0x5c4033, 1)
    g.fillRect(0, 0, 16, 16)
    g.fillStyle(0x7cba5c, 1)
    g.fillRect(0, 0, 16, 3)
    g.generateTexture('ground', 16, 16)
    g.clear()

    // platform (16x16)
    g.fillStyle(0x8b6f47, 1)
    g.fillRect(0, 0, 16, 16)
    g.fillStyle(0xa08060, 1)
    g.fillRect(0, 0, 16, 2)
    g.fillStyle(0x6b4f2f, 1)
    g.fillRect(0, 15, 16, 1)
    g.generateTexture('platform', 16, 16)
    g.clear()

    // spike (16x16)
    g.fillStyle(0xd63031, 1)
    g.beginPath()
    g.moveTo(8, 0)
    g.lineTo(16, 16)
    g.lineTo(0, 16)
    g.closePath()
    g.fillPath()
    g.generateTexture('spike', 16, 16)
    g.clear()

    // heart (12x12)
    g.fillStyle(0xe74c3c, 1)
    g.fillCircle(3, 3, 3)
    g.fillCircle(9, 3, 3)
    g.beginPath()
    g.moveTo(0, 4)
    g.lineTo(6, 11)
    g.lineTo(12, 4)
    g.closePath()
    g.fillPath()
    g.generateTexture('heart', 12, 12)
    g.clear()

    // flag (16x32)
    g.fillStyle(0x333333, 1)
    g.fillRect(1, 0, 2, 32)
    g.fillStyle(0xe74c3c, 1)
    g.fillRect(3, 0, 12, 10)
    g.fillStyle(0xffffff, 1)
    g.fillRect(7, 3, 4, 4)
    g.generateTexture('flag', 16, 32)
    g.clear()

    // cloud (48x20)
    g.fillStyle(0xffffff, 0.7)
    g.fillRoundedRect(0, 4, 48, 16, 8)
    g.fillRoundedRect(8, 0, 32, 12, 6)
    g.generateTexture('cloud', 48, 20)
    g.clear()

    // star (8x8)
    g.fillStyle(0xffd700, 1)
    g.fillRect(3, 0, 2, 8)
    g.fillRect(0, 3, 8, 2)
    g.generateTexture('star', 8, 8)
    g.clear()

    g.destroy()

    this.scene.start('MenuScene')
  }
}
